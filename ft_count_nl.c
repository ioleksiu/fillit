#include "fillit.h"

int		ft_count_nl(char *s, int l, int f)
{
	int	n;

	n = 0;
	while (l-- > 0)
	{
		if (*s == '\n')
			n++;
		s++;
	}
	if (f == 1 && n == 4)
		return (0);
	if (f > 1 && n == ((4 * f) + (f - 1)))
		return (0);
	ft_error();
	return (-1);
}
