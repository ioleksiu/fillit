#include "fillit.h"

int		ft_figure_ck(char *s, int i, int h)
{
	while (*s != '\0')
	{
		while (h != 4 && *s != '\0')
		{
			while (*s != '#' && *s)
				s++;
			h++;
			if (*(s - 5) == '#')
				i++;
			if (*(s - 1) == '#')
				i++;
			if (*(s + 1) == '#')
				i++;
			if (*(s + 5) == '#')
				i++;
			if (*s != '\0')
				s++;
			else
				return (0);
		}
		s++;
	}
	if (i != 6 && i != 8)
		ft_error();
	return (0);
}
