#include "fillit.h"

int		ft_nl_location(char *s, int f)
{
	int	j;

	j = 0;
	while (f-- > 0 || *s != '\0')
	{
		while (j != 4)
		{
			if (*s == '\n')
				s += 5;
			else
				s += 4;
			if (*s == '\n')
				j++;
			if (*s != '\n')
				ft_error();
		}
		if (f > 0 && (*(s + 1) != '\n' || *(s + 2) == '\n'))
			ft_error();
		s++;
		j = 0;
	}
	return (0);
}
