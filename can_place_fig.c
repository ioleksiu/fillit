#include "fillit.h"

int		can_place_fig(char **map, t_xy *a, int x, int y)
{
	if (map[x + a->x[0]][y + a->y[0]] == '.' &&
		map[x + a->x[1]][y + a->y[1]] == '.' &&
		map[x + a->x[2]][y + a->y[2]] == '.' &&
		map[x + a->x[3]][y + a->y[3]] == '.')
	{
		map[x + (a->x[0])][y + (a->y[0])] = a->c;
		map[x + (a->x[1])][y + (a->y[1])] = a->c;
		map[x + (a->x[2])][y + (a->y[2])] = a->c;
		map[x + (a->x[3])][y + (a->y[3])] = a->c;
		return (1);
	}
	else
		return (0);
}
