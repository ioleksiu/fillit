#include "fillit.h"

void	print_map(char **map, int size)
{
	int i;
	int	j;

	i = 0;
	j = -1;
	while (j++ < size)
	{
		while (i < size)
			write(1, &map[i++][j], 1);
		write(1, "\n", 1);
		i = 0;
	}
}
