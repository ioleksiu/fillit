#include "fillit.h"

int		ft_figure_nb(int l)
{
	int	n;

	n = 0;
	if (l == 20)
		return (1);
	if (l > 20)
	{
		l -= 20;
		if (l % 21 != 0)
			ft_error();
		l /= 21;
		l++;
		return (l);
	}
	ft_error();
	return (-1);
}
